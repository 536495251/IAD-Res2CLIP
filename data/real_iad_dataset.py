def _default_meta_path():
    return os.path.join(os.path.dirname(__file__), "real_iad_meta.json")


class RealIADDataset(data.Dataset):
    """Real-IAD dataset for inference.

    Each 'item' represents one or all views of one sample, depending on view_mode.

    Args:
        root:           dataset root directory (contains Train/ etc.)
        transform:      image transform (torchvision.Compose)
        target_transform: mask transform (torchvision.Compose)
        split:          'train' | 'test_a' | 'test_b'
        view_mode:      'single' — each item = one view, len = N×5
                        'multi'  — each item = all 5 views stacked, len = N
        view_index:     when view_mode='single', which view to load (0-4).
                        None means iterate over all 5 views (len = N×5).
        categories:     None = all categories; or a list of category names to filter.
        meta_path:      path to real_iad_meta.json (default: data/real_iad_meta.json)
    """
    def __init__(self, root, transform, target_transform, split='train',
                 view_mode='single', view_index=None, categories=None,
                 meta_path=None):

        self.root = root
        self.transform = transform
        self.target_transform = target_transform
        self.split = split
        self.view_mode = view_mode
        self.view_index = view_index  # only used when view_mode='single'

        # Load metadata
        meta_path = meta_path or _default_meta_path()
        meta_info = json.load(open(meta_path, 'r'))
        split_key = split.lower().replace(" ", "_")
        if split_key not in meta_info:
            raise ValueError(f"Split '{split}' not found in {meta_path}. "
                             f"Available: {list(meta_info.keys())}")
        meta_info = meta_info[split_key]

        # Filter categories if specified
        self.categories = categories or list(meta_info.keys())
        self.obj_list = self.categories
        self.class_name_map_class_id = {k: i for i, k in enumerate(self.obj_list)}

        # Build flat index: list of (cls_name, sample_dir)
        self.samples = []
        for cls_name in self.categories:
            if cls_name not in meta_info:
                continue
            normal_samples = meta_info[cls_name].get("normal", [])
            for sample_dir in normal_samples:
                self.samples.append((cls_name, sample_dir))

        # Views to load per sample
        self.num_views = 5  # Real-IAD always has 5 views (0.png ~ 4.png)

    def __len__(self):
        if self.view_mode == 'single' and self.view_index is None:
            # Iterate over all 5 views for each sample
            return len(self.samples) * self.num_views
        else:
            return len(self.samples)

    def _load_one_view(self, cls_name, sample_dir, view_idx):
        """Load a single view image and return transformed tensors."""
        img_path = os.path.join(self.root, sample_dir, f"{view_idx}.png")
        img = Image.open(img_path).convert('RGB')
        # Train set has only normal samples → zero mask
        h, w = img.size
        img_mask = Image.fromarray(np.zeros((w, h), dtype=np.uint8), mode='L')

        if self.transform is not None:
            img = self.transform(img)
        if self.target_transform is not None:
            img_mask = self.target_transform(img_mask)

        # Also return original view size for reference
        return img, img_mask

    def __getitem__(self, index):
        if self.view_mode == 'multi':
            # Return all 5 views stacked
            cls_name, sample_dir = self.samples[index]
            imgs, masks = [], []
            for v in range(self.num_views):
                img, mask = self._load_one_view(cls_name, sample_dir, v)
                imgs.append(img.unsqueeze(0))
                masks.append(mask.unsqueeze(0))
            imgs = torch.cat(imgs, dim=0)    # [5, C, H, W]
            masks = torch.cat(masks, dim=0)  # [5, 1, H, W]

            return {
                'images': imgs,
                'img_masks': masks,
                'cls_name': cls_name,
                'anomaly': 0,
                'group_folder': sample_dir,
                'cls_id': self.class_name_map_class_id[cls_name],
            }

        else:
            # Single view mode
            if self.view_index is not None:
                # Fixed view for all samples
                cls_name, sample_dir = self.samples[index]
                view_idx = self.view_index
            else:
                # Iterate over all 5 views
                sample_idx = index // self.num_views
                view_idx = index % self.num_views
                cls_name, sample_dir = self.samples[sample_idx]

            img, img_mask = self._load_one_view(cls_name, sample_dir, view_idx)

            return {
                'img': img,
                'img_mask': img_mask,
                'cls_name': cls_name,
                'anomaly': 0,
                'img_path': os.path.join(self.root, sample_dir, f"{view_idx}.png"),
                'group_folder': sample_dir,
                'view_idx': view_idx,
                'cls_id': self.class_name_map_class_id[cls_name],
            }


class RealIADRefDataset(data.Dataset):
    """Real-IAD dataset for training (or memory bank construction).

    Returns (query_img, ref_img, mask) per view, where ref_img is a randomly
    sampled normal image from the same class (and optionally same view).

    Args:
        same_view_ref: if True, reference comes from the same view index.
                       if False, reference can be any view of any normal sample.
    """
    def __init__(self, root, transform, target_transform, split='train',
                 same_view_ref=False, categories=None, meta_path=None):

        self.root = root
        self.transform = transform
        self.target_transform = target_transform
        self.split = split
        self.same_view_ref = same_view_ref

        # Load metadata
        meta_path = meta_path or _default_meta_path()
        meta_info = json.load(open(meta_path, 'r'))
        split_key = split.lower().replace(" ", "_")
        if split_key not in meta_info:
            raise ValueError(f"Split '{split}' not found in {meta_path}")
        meta_info = meta_info[split_key]

        self.categories = categories or list(meta_info.keys())
        self.obj_list = self.categories
        self.class_name_map_class_id = {k: i for i, k in enumerate(self.obj_list)}

        # Build sample list: (cls_name, sample_dir)
        self.samples = []
        for cls_name in self.categories:
            if cls_name not in meta_info:
                continue
            normal_samples = meta_info[cls_name].get("normal", [])
            for sample_dir in normal_samples:
                self.samples.append((cls_name, sample_dir))

        # Build per-class normal sample index for reference sampling
        self.normal_indices = {cls: [] for cls in self.categories}
        for i, (cls_name, _) in enumerate(self.samples):
            if cls_name in self.normal_indices:
                self.normal_indices[cls_name].append(i)

        self.num_views = 5

    def __len__(self):
        # Each sample × each view = one item
        return len(self.samples) * self.num_views

    def __getitem__(self, index):
        sample_idx = index // self.num_views
        view_idx = index % self.num_views

        cls_name, sample_dir = self.samples[sample_idx]

        # Load query image (one view)
        img_path = os.path.join(self.root, sample_dir, f"{view_idx}.png")
        img = Image.open(img_path).convert('RGB')
        # Zero mask (all training samples are normal)
        h, w = img.size
        img_mask = Image.fromarray(np.zeros((w, h), dtype=np.uint8), mode='L')

        # Sample a normal reference from the same class
        normal_pool = [i for i in self.normal_indices[cls_name] if i != sample_idx]
        if not normal_pool:
            normal_pool = self.normal_indices[cls_name]
        ref_sample_idx = random.choice(normal_pool)
        ref_cls_name, ref_sample_dir = self.samples[ref_sample_idx]

        if self.same_view_ref:
            # Same view index for reference
            ref_img_path = os.path.join(self.root, ref_sample_dir, f"{view_idx}.png")
        else:
            # Random view for reference
            ref_view_idx = random.randint(0, self.num_views - 1)
            ref_img_path = os.path.join(self.root, ref_sample_dir, f"{ref_view_idx}.png")
        ref_img = Image.open(ref_img_path).convert('RGB')

        # Apply transforms
        if self.transform is not None:
            img = self.transform(img)
            ref_img = self.transform(ref_img)
        if self.target_transform is not None:
            img_mask = self.target_transform(img_mask)

        return {
            'img': img,
            'ref_img': ref_img,
            'img_mask': img_mask,
            'cls_name': cls_name,
            'anomaly': 0,
            'img_path': img_path,
            'view_idx': view_idx,
            'cls_id': self.class_name_map_class_id[cls_name],
        }
